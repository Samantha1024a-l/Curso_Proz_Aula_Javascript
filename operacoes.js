function operacoesBasicas(numeroA, numeroB) {
    console.log(`${numeroA} + ${numeroB} = ${numeroA + numeroB}`);
    console.log(`${numeroA} - ${numeroB} = ${numeroA - numeroB}`);
    console.log(`${numeroA} x ${numeroB} = ${numeroA * numeroB}`);
    console.log(`${numeroA} / ${numeroB} = ${numeroA / numeroB}`);
}

// Exemplo de uso
operacoesBasicas(4, 5);
